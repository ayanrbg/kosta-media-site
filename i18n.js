window.KM_DETECT = function() {
  try {
    const saved = localStorage.getItem("km_lang");
    if (saved && window.KM_I18N[saved]) return saved;
  } catch (e) {}
  const nav = (navigator.language || "en").toLowerCase();
  if (nav.startsWith("ru")) return "ru";
  if (nav.startsWith("uz")) return "uz";
  if (nav.startsWith("kk") || nav.startsWith("kz")) return "kz";
  if (nav.startsWith("ky") || nav.startsWith("kg")) return "kg";
  return "en";
};
